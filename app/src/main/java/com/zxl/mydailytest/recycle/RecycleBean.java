package com.zxl.mydailytest.recycle;

/**
 * @author crazyZhangxl on 2018/12/10.
 * Describe:
 */
public class RecycleBean {
    private int first;
    private int second;

    public RecycleBean(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
